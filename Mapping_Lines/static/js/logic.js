// create a base layer that holds both maps.
let baseMaps = {
  Street: streets,
  Dark: darkmap
};

// Create the map object with center and zoom level.
let map = L.map('mapid', {
  center: [30, 30],
   zoom: 2,
   layers: [streets]
})

// Pass our map layers into our layers control and add the layers control to the map.
L.control.layers(baseMaps).addTo(map);

// Accessing the airport GeoJSON URL
let airportData = "https://raw.githubusercontent.com/shrese/Mapping_Earthquakes/main/majorAirports.json";

// Grabbing our GeoJSON data.
d3.json(airportData).then(function (data) {
  console.log(data);
  // creating a geojson layer with the retireved data
  L.geoJson(data, {
    onEachFeature: function(feature, layer) {
      layer.bindPopup("<h2>" + feature.properties.name +
    "</h2><hr><p>" + feature.properties.city + ", " + feature.properties.country + "</h2>");
    }
  }).addTo(map);
  });

  
